-- Creates the public.profiles row for every new auth.users row, always as
-- CUSTOMER, ignoring any role the client may have sent in signUp() metadata.
-- This is what guarantees public registration can never self-assign a
-- privileged role.

create or replace function public.handle_new_user()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  insert into public.profiles (id, email, full_name, role)
  values (
    new.id,
    new.email,
    new.raw_user_meta_data ->> 'full_name',
    'CUSTOMER'
  );
  return new;
end;
$$;

drop trigger if exists on_auth_user_created on auth.users;
create trigger on_auth_user_created
  after insert on auth.users
  for each row execute function public.handle_new_user();
